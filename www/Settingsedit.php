	// Load row values from recordset
	function LoadRowValues(&$rs) {
		global $conn;
		if (!$rs || $rs->EOF) return;

		// Call Row Selected event
		$row = &$rs->fields;
		$this->Row_Selected($row);
		$this->Item->setDbValue($rs->fields('Item'));
		$this->Value->setDbValue($rs->fields('Value'));
